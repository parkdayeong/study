// [★] : == vs === 차이점 비교하기


// [1] : 꼭 기억하자!
// ==		--> 값이 같으면 같은거다.
// === 		--> 값만 같아서는 안되고, 자료형(Data Type)까지 모두 일치해야만 한다.


// [2] : 실습
alert( 10 == "10" );  	// true
alert( 10 === "10" );	// false --> 값만 같아서는 안되고, 자료형까지 같아야 true


// [3] : != vs !==
alert( 10 != "10" );		// false
alert( 10 !== "10" );		// true

